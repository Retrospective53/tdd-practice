const add = (num1, num2) => {
    return num1 + num2
}

const substract = (num1, num2) => {
    return num1 - num2
}

const multiply = (num1, num2) => {
    return num1 * num2
}

const divide = (num1, num2) => {
    return num1 / num2
}

const calculator = {
    add,
    substract,
    multiply,
    divide
}

export default calculator;